const mongoose = require('mongoose')

const userLoginSchema = new mongoose.Schema(
    {
        firstName: {
            type: String,
            required: true
        },
        lastName: {
            type: String,
            required: true
        },
        email: {
            type: String,
            required: true,
            unique: true
        },
        password: {
            type: String,
            required: true
        }
    }, { collection: 'user' }
)

module.exports = mongoose.model('user', userLoginSchema)